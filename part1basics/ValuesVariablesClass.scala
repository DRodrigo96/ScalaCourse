package lectures.part1basics

object ValuesVariablesClass extends App {

    val entero: Int = 42
    println("entero", entero)

    val flotante: Float = 55;
    println("flotante", flotante)

    val cadena: String = "miString"
    println("cadena", cadena)

    val caracter: Char = 'a'
    println("caracter", caracter)

    val doble: Double = 99
    println("doble", doble)

    val booleano: Boolean = true
    println("booleano", booleano)

    val largo: Long = 12312
    println("largo", largo)

    val corto: Short = 3276
    println("corto", corto)

    val byte: Byte = 127
    println("byte", byte)
}
